package com.myspace.loanorigination;

public class Person {
	private java.lang.String name;
	private java.lang.String address;
	private java.lang.Integer age;

	private java.lang.Boolean eligibleToApplyLoan;

	/**
	 * @return the name
	 */
	public java.lang.String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(java.lang.String name) {
		this.name = name;
	}

	/**
	 * @return the address
	 */
	public java.lang.String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(java.lang.String address) {
		this.address = address;
	}

	/**
	 * @return the age
	 */
	public java.lang.Integer getAge() {
		return age;
	}

	/**
	 * @param age the age to set
	 */
	public void setAge(java.lang.Integer age) {
		this.age = age;
	}

	/**
	 * @return the eligibleToApplyLoan
	 */
	public java.lang.Boolean getEligibleToApplyLoan() {
		return eligibleToApplyLoan;
	}

	/**
	 * @param eligibleToApplyLoan the eligibleToApplyLoan to set
	 */
	public void setEligibleToApplyLoan(java.lang.Boolean eligibleToApplyLoan) {
		this.eligibleToApplyLoan = eligibleToApplyLoan;
	}
	
	

}
